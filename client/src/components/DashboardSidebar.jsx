const SIDEBAR_WIDTH = 240
const EDGE_ZONE = 12

export default function DashboardSidebar() {
  const [open, setOpen] = useState(false)
  const [pinned, setPinned] = useState(false)
  const [role, setRole] = useState(null)
  const [loading, setLoading] = useState(true)
  const sidebarRef = useRef(null)
  const pathname = usePathname()
  const supabase = createClient()

  // Fetch auth state and role
  useEffect(() => {
    let cancelled = false

    async function fetchRole() {
      const { data: { user } } = await supabase.auth.getUser()
      if (!user || cancelled) {
        setRole(null)
        setLoading(false)
        return
      }

      const { data: profile } = await supabase
        .from('user_profiles')
        .select('role')
        .eq('id', user.id)
        .maybeSingle()

      if (!cancelled) {
        setRole(profile?.role || null)
        setLoading(false)
      }
    }

    fetchRole()

    const { data: { subscription } } = supabase.auth.onAuthStateChange(() => {
      fetchRole()
    })

    return () => {
      cancelled = true
      subscription.unsubscribe()
    }
  }, [])

  // Close on route change (unless pinned)
  useEffect(() => {
    if (!pinned) setOpen(false)
  }, [pathname, pinned])

  // Edge-hover detection
  const handleMouseMove = useCallback(
    (e) => {
      if (pinned || !role) return
      if (e.clientX <= EDGE_ZONE) {
        setOpen(true)
      }
    },
    [pinned, role]
  )

  useEffect(() => {
    window.addEventListener('mousemove', handleMouseMove)
    return () => window.removeEventListener('mousemove', handleMouseMove)
  }, [handleMouseMove])

  const handleMouseLeave = () => {
    if (!pinned) setOpen(false)
  }

  const togglePin = () => {
    if (pinned) {
      setPinned(false)
      setOpen(false)
    } else {
      setPinned(true)
      setOpen(true)
    }
  }

  // Don't render anything if not logged in or still loading
  if (loading || !role) return null

  const config = ROLE_CONFIG[role] || ROLE_CONFIG.candidate
  const { items, label: roleLabel } = config

  return (
    <>
      {/* Overlay */}
      {open && !pinned && (
        <div
          className="fixed inset-0 z-40 bg-black/10"
          onClick={() => setOpen(false)}
        />
      )}

      {/* Sidebar */}
      <aside
        ref={sidebarRef}
        onMouseLeave={handleMouseLeave}
        className={`fixed top-0 left-0 z-50 h-full bg-white/95 backdrop-blur-xl border-r border-gray-200/80 shadow-2xl flex flex-col transition-transform duration-300 ease-out ${
          open ? 'translate-x-0' : '-translate-x-full'
        }`}
        style={{ width: SIDEBAR_WIDTH }}
      >
        {/* Header */}
        <div className="flex items-center justify-between px-4 py-4 border-b border-gray-100">
          <div>
            <p className="text-[10px] uppercase tracking-widest text-gray-400 font-semibold">
              {roleLabel}
            </p>
            <p className="text-sm font-bold text-gray-900 mt-0.5">MyTechZ</p>
          </div>
          <button
            type="button"
            onClick={togglePin}
            className={`w-7 h-7 rounded-lg flex items-center justify-center transition-colors cursor-pointer ${
              pinned
                ? 'bg-blue-50 text-blue-600'
                : 'bg-gray-100 text-gray-400 hover:text-gray-600 hover:bg-gray-200'
            }`}
            title={pinned ? 'Unpin sidebar' : 'Pin sidebar open'}
          >
            {pinned ? (
              <svg className="w-4 h-4" fill="currentColor" viewBox="0 0 24 24">
                <path d="M16 12V4h1V2H7v2h1v8l-2 2v2h5.2v6h1.6v-6H18v-2l-2-2z" />
              </svg>
            ) : (
              <svg className="w-4 h-4" fill="none" stroke="currentColor" strokeWidth={1.8} viewBox="0 0 24 24">
                <path strokeLinecap="round" strokeLinejoin="round" d="M16 12V4h1V2H7v2h1v8l-2 2v2h5.2v6h1.6v-6H18v-2l-2-2z" />
              </svg>
            )}
          </button>
        </div>

        {/* Nav items */}
        <nav className="flex-1 overflow-y-auto px-3 py-3 space-y-1">
          {items.map((item) => {
            const isActive =
              pathname === item.href ||
              (item.href !== '/' && pathname.startsWith(item.href + '/'))

            return (
              <Link
                key={item.href}
                href={item.href}
                className={`flex items-center gap-3 px-3 py-2.5 rounded-xl text-sm font-medium transition-all duration-150 group ${
                  isActive
                    ? 'bg-blue-50 text-blue-700'
                    : 'text-gray-600 hover:bg-gray-50 hover:text-gray-900'
                }`}
              >
                <span
                  className={`shrink-0 transition-colors ${
                    isActive
                      ? 'text-blue-600'
                      : 'text-gray-400 group-hover:text-gray-600'
                  }`}
                >
                  {item.icon}
                </span>
                <span>{item.label}</span>
                {isActive && (
                  <span className="ml-auto w-1.5 h-1.5 rounded-full bg-blue-600" />
                )}
              </Link>
            )
          })}
        </nav>

        {/* Footer */}
        <div className="px-4 py-3 border-t border-gray-100">
          <Link
            href="/"
            className="flex items-center gap-2 text-xs text-gray-400 hover:text-gray-600 transition-colors"
          >
            <svg className="w-4 h-4" fill="none" stroke="currentColor" strokeWidth={1.7} viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" d="M15 19l-7-7 7-7" />
            </svg>
            Back to home
          </Link>
        </div>
      </aside>

      {/* Edge indicator */}
      {!open && (
        <div
          className="fixed top-1/2 left-0 z-40 -translate-y-1/2 w-1 h-12 rounded-r-full bg-blue-500/30 hover:bg-blue-500/60 hover:h-16 transition-all duration-200 cursor-pointer"
          onClick={() => setOpen(true)}
        />
      )}
    </>
  )
}
